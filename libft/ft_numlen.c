#include "libft.h"

int		ft_numlen(uint64_t n, int base)
{
	int			i;

	i = (n <= 0) ? 1 : 0;
	if (base < 2)
		return (0);
	while (n)
	{
		n = n / base;
		i++;
	}
	return (i);
}
